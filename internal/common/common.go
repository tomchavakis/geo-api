package common

// Float64Ptr returns the pointer to a float64.
func Float64Ptr(v float64) *float64 {
	return &v
}

// Int64Ptr returns the pointer to an int64.
func Int64Ptr(v int64) *int64 {
	return &v
}

// Int32Ptr returns the pointer to an int32.
func Int32Ptr(v int32) *int32 {
	return &v
}

// IntPtr returns the pointer to an int
func IntPtr(v int) *int {
	return &v
}

// StringPtr returns the pointer to a string.
func StringPtr(v string) *string {
	return &v
}

// BoolPtr returns the pointer to a string.
func BoolPtr(v bool) *bool {
	return &v
}
